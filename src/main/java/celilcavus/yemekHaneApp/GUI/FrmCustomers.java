    private void GetList()
    {
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        var data = customerRepository.GetAll();
        for(var item : data)
        {
            Object[] s = {item.getId(),item.getFirstName(),item.getLastName(),item.getEmail(),item.getPhone()};
            model.addRow(s);
        }
    }
